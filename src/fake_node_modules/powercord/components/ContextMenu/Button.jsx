const { React, getModule, contextMenu } = require('powercord/webpack');
const { getOwnerInstance, waitFor } = require('powercord/util');

let oldState = null;

module.exports = class ButtonItem extends React.PureComponent {
  constructor (props) {
    super(props);

    this.state = oldState || {
      itemClasses: props.itemClasses || '',
      appOwnerInstance: ''
    };
  }

  async componentDidMount () {
    const itemClasses = (await getModule([ 'itemToggle', 'checkbox' ]));

    if (!this.state.itemClasses) {
      this.setState({ itemClasses });
    }

    waitFor(`.${(await getModule([ 'app' ])).app.replace(/ /g, '.')}`)
      .then(app => this.state.appOwnerInstance = getOwnerInstance(app));
  }

  componentWillUnmount () {
    oldState = this.state;
  }

  onClick () {
    if (this.props.disabled) {
      this.state.appOwnerInstance.shake(600, 5);
    } else {
      contextMenu.closeContextMenu();
      this.props.onClick();
    }
  }

  render () {
    const { itemClasses } = this.state;
    const button = (
      <div
        className={[
          itemClasses.item,
          this.props.image ? itemClasses.itemImage : '',
          itemClasses.clickable,
          this.props.disabled ? `${itemClasses.disabled} disabled` : ''
        ].join(' ')}
        role='button'
        onClick={this.onClick.bind(this)}
      >
        <span style={{ color: this.props.highlight }}>
          {this.props.name}
        </span>
        {this.props.image
          ? this.getButtonImage()
          : <div className={itemClasses.hint}>
            {this.props.hint}
          </div>}
      </div>
    );

    if (this.props.seperate) {
      return (
        <div className={`${itemClasses.itemGroup} seperated`}>
          {button}
        </div>
      );
    }

    return button;
  }

  getButtonImage () {
    return (
      this.props.image.startsWith('fa-')
        ? <div style={{ cursor: 'pointer !important' }}
          class={`${this.props.image.endsWith('-regular')
            ? 'far'
            : this.props.image.endsWith('-brand')
              ? 'fab'
              : 'fas'}
            ${this.props.image.replace(/-regular|-brand/gi, '')} fa-fw`} />
        : <img alt className={`${this.state.itemClasses.image} ${this.props.className || ''}`}
          src={this.props.image} />
    );
  }
};
