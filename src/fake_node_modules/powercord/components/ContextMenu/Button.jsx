const { React, getModule } = require('powercord/webpack');
const { getOwnerInstance, waitFor } = require('powercord/util');

const ItemGroup = require('./ItemGroup.jsx');

let classes = null;
let appOwnerInstance = null;
setImmediate(async () => {
  classes = {
    ...await getModule([ 'scrollbar', 'scrollerWrap' ]),
    ...await getModule([ 'itemToggle', 'checkbox' ])
  };

  waitFor(`.${(await getModule([ 'app' ])).app.replace(/ /g, '.')}`)
    .then(app => appOwnerInstance = getOwnerInstance(app));
});

module.exports = class ButtonItem extends React.PureComponent {
  onClick () {
    if (this.props.disabled) {
      appOwnerInstance.shake(600, 5);
    } else {
      this.props.onClick();
    }
  }

  render () {
    const button = (
      <div
        className={[
          classes.item,
          this.props.image ? classes.itemImage : null,
          classes.clickable,
          this.props.disabled ? classes.disabled : null
        ].filter(Boolean).join(' ')}
        role='button'
        onClick={this.onClick.bind(this)}
      >
        <span className={classes.label} style={{ color: this.props.highlight }}>
          {this.props.name}
        </span>

        {this.props.image
          ? this.getButtonImage()
          : <div className={classes.hint}>
            {this.props.hint}
          </div>}
      </div>
    );

    if (this.props.seperate) {
      return React.createElement(ItemGroup, {
        children: [ button ]
      });
    }

    return button;
  }

  getButtonImage () {
    return (
      this.props.image.startsWith('fa-')
        ? <div style={{ cursor: 'pointer !important' }}
          className={`${this.getFontAwesomePrefix(this.props.image)}
            ${this.props.image.replace(/-regular|-light|-brand/gi, '')} fa-fw`} />
        : <img alt className={[ classes.image, this.props.className || null ].filter(Boolean).join(' ')}
          src={this.props.image} />
    );
  }

  getFontAwesomePrefix (str) {
    const suffixes = [ 'regular', 'light', 'brand' ];
    const stringParts = str.split('-');
    const suffix = suffixes.find(suffix => suffix === stringParts[stringParts.length - 1]);

    if (suffixes[suffix]) {
      return `fa${suffix.charAt(0).toLowerCase()}`;
    }

    return 'fas';
  }
};
