const { React, getModule } = require('powercord/webpack');

let oldState = null;

module.exports = class CheckboxItem extends React.PureComponent {
  constructor (props) {
    super(props);

    this.state = oldState || {
      itemClasses: props.itemClasses || '',
      checkboxClasses: '',
      checked: props.defaultState
    };
  }

  async componentDidMount () {
    const itemClasses = (await getModule([ 'itemToggle', 'checkbox' ]));

    if (!this.state.itemClasses && !this.state.checkboxClasses) {
      this.setState({ itemClasses,
        checkboxClasses: (await getModule([ 'checkboxElement' ]))
      });
    }
  }

  componentWillUnmount () {
    oldState = this.state;
  }

  onClick () {
    const checked = !this.state.checked;

    this.props.onToggle(checked);
    this.setState({ checked });
  }

  render () {
    const { itemClasses, checkboxClasses } = this.state;
    const checkbox = (
      <div
        className={[ itemClasses.item, itemClasses.itemToggle ].join(' ')}
        role='button'
        onClick={() => this.onClick()}
      >
        <div className={itemClasses.label}>
          {this.props.name}
        </div>

        <div className={[ checkboxClasses.checkbox, itemClasses.checkbox ].join(' ')} role='button'>
          <div className={checkboxClasses.checkboxInner}>
            <input className={checkboxClasses.checkboxElement} type='checkbox' checked={this.state.checked}/>
            <span/>
          </div>
        </div>
      </div>
    );

    if (this.props.seperate) {
      return (
        <div className={`${itemClasses.itemGroup} seperated`}>
          {checkbox}
        </div>
      );
    }

    return checkbox;
  }
};
