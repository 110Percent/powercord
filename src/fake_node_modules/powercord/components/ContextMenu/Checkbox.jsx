const { React } = require('powercord/webpack');

module.exports = class CheckboxItem extends React.PureComponent {
  constructor (props) {
    super(props);

    this.state = {
      checked: props.defaultState
    };
  }

  onClick () {
    const checked = !this.state.checked;

    this.props.onToggle(checked);
    this.setState({ checked });
  }

  render () {
    const checkbox = (
      <div
        className='pc-item pc-itemToggle item-1Yvehc itemToggle-S7XGOQ'
        role='button'
        onClick={() => this.onClick()}
      >
        <div className='pc-label label-JWQiNe'>
          {this.props.name}
        </div>

        <div className='checkbox-3kaeSU pc-checkbox checkbox-3EVISJ pc-checkbox' role='button'>
          <div className='checkboxInner-3yjcPe pc-checkboxInner'>
            <input className='checkboxElement-1qV33p pc-checkboxElement' type='checkbox' checked={this.state.checked}/>
            <span/>
          </div>
        </div>
      </div>
    );

    if (this.props.seperate) {
      return (
        <div className='pc-itemGroup itemGroup-1tL0uz seperated'>
          {checkbox}
        </div>
      );
    }

    return checkbox;
  }
};
