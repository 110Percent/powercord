const { React } = require('powercord/webpack');
const { sleep } = require('powercord/util');

module.exports = class Toast extends React.Component {
  constructor (props) {
    super(props);

    this.state = {
      header: props.header,
      buttons: props.buttons,
      content: props.content
    };
  }

  onBtnClick (onClick) {
    return onClick(state => new Promise(async (resolve) => {
      this.setState(state);

      if (state.fade) {
        await sleep(200);
      }
      if ('leaving' in state) {
        await sleep(500);
      }

      resolve();
    }));
  }

  render () {
    const { props, state } = this;

    return (
      <div
        className={`powercord-toast${state.leaving ? ' leaving' : ''}`}
        style={props.style}
      >
        {state.header && (
          <div className='header'>
            {state.header}
          </div>
        )}

        <div className={state.fade ? `body fade${state.fade}` : 'body'}>
          {state.content && (
            <div className='content'>
              {state.content}
            </div>
          )}

          <div className='buttons'>
            {state.buttons.map(button => (
              <div className='button' onClick={this.onBtnClick.bind(this, button.onClick)}>
                {button.text}
              </div>
            ))}
          </div>
        </div>
      </div>
    );
  }
};
