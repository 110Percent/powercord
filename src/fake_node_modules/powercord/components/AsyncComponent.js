const { React } = require('powercord/webpack');

module.exports = class AsyncComponent extends React.Component {
  constructor (props) {
    super(props);
    this.state = {
      Component: null
    };
  }

  async componentDidMount () {
    this.setState({
      Component: await this.props._provider()
    });
  }

  render () {
    const { Component } = this.state;
    if (Component) {
      return React.createElement(Component, this.props);
    }
    return null;
  }

  /**
   * Creates an AsyncComponent from a promise
   * @param {Promise<Component>} promise Promise of a React component
   */
  static from (promise) {
    return (props) => React.createElement(AsyncComponent, {
      _provider: () => promise,
      ...props
    });
  }
};
