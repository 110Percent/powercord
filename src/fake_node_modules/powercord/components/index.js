const { React, getModuleByDisplayName } = require('powercord/webpack');
const AC = require('./AsyncComponent');

require('fs')
  .readdirSync(__dirname)
  .filter(file => file !== 'index.js')
  .forEach(filename => {
    const moduleName = filename.split('.')[0];
    exports[moduleName] = require(`${__dirname}/${filename}`);
  });

// Add some util components
Object.assign(exports, {
  Button: (props) => React.createElement(AC, {
    _provider: () => getModuleByDisplayName('DropdownButton'),
    ...props
  }),
  Tooltip: (props) => React.createElement(AC, {
    _provider: () => getModuleByDisplayName('Tooltip'),
    ...props
  }),
  Switch: (props) => React.createElement(AC, {
    _provider: () => getModuleByDisplayName('Switch'),
    ...props
  }),
  Spinner: (props) => React.createElement(AC, {
    _provider: () => getModuleByDisplayName('Spinner'),
    ...props
  }),
  Divider: (props) => React.createElement(AC, {
    _provider: () => getModuleByDisplayName('FormDivider'),
    ...props
  })
});
