const { getModuleByDisplayName } = require('powercord/webpack');

require('fs')
  .readdirSync(__dirname)
  .filter(file => file !== 'index.js')
  .forEach(filename => {
    const moduleName = filename.split('.')[0];
    exports[moduleName] = require(`${__dirname}/${filename}`);
  });

// Add some util components
Object.assign(exports, {
  Button: getModuleByDisplayName('DropdownButton'),
  Tooltip: getModuleByDisplayName('Tooltip'),
  Switch: getModuleByDisplayName('Switch'),
  Spinner: getModuleByDisplayName('Spinner')
});
